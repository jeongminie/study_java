package com.jeongmini.study.ch03;

public class AssignmentOperatorExample {

	public static void main(String[] args) {

		int result = 0;
		
		result += 10;
		System.out.println(result);
		
		int result2 = 0;
		
		result2 -= 10;
		System.out.println(result2);
		
		int result3 = 0;
		
		result3 *= 10;
		System.out.println(result3);
			

	}

}
